    {
        $request->validate([
            'email'=>'required',
            'password'=>'required',
        ]);
        $credentials = $request->only('email','password');
        if(Auth::attempt($credentials)){
            return redirect()->route('platform.index');
        }
        return redirect()->back()->with('message','  عدرا البيانات غير متوفرة !  ');
    }

